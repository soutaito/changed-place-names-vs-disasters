class App extends React.Component {
  constructor (props) {
    super(props)
    this.state = {
      data: [],
    }
  }

  componentDidMount () {
    const geocodingBaseURL = 'https://maps.googleapis.com/maps/api/geocode/json?key=AIzaSyAt-DUVu9Y567PcAmw50gG5l6-QLepfNIQ&latlng='
    const promises = oldAddresses.map(v => {
      return fetch(geocodingBaseURL + v[1] + ',' + v[2]).then((response) => response.json()).then((json) => {
        console.log(json)
        const sublocality = json.results[0].address_components.find(w => {
          return w.types.find(t => {
            return t === 'sublocality'
          })
        })
        const locality = json.results[0].address_components.find(w => {
          return w.types.find(t => {
            return t === 'locality'
          })
        })
        const administrative_area_level_1 = json.results[0].address_components.find(w => {
          return w.types.find(t => {
            return t === 'administrative_area_level_1'
          })
        })
        return [json.results[0].formatted_address, sublocality.long_name, locality.long_name, administrative_area_level_1.long_name]
      }).catch((error) => console.log(error))
    })

    Promise.all(promises).then(results => {
      const data = oldAddresses.slice()
      data.forEach((v, i) => {
        if (results[i]) {
          data[i].push(results[i][0])
          data[i].push(results[i][1])
          data[i].push(results[i][2])
          data[i].push(results[i][3])
          data[i].push(Number(results[i][0].indexOf(data[i][0]) > -1))
        }
      })
      this.setState({ data: data })
    })
  }

  render () {
    return (
      <table>
        <tbody>
        {this.state.data.map((v, i) => {
          return <tr key={i}>
            {
              v.map(w => <td>{w}</td>)
            }
          </tr>
        })}
        </tbody>
      </table>
    )
  }
}

export default App
